//Extension fonksiyonlar kotlin içinde bulunan sınıfları genişletmek için kullanılan pratik bir yöntemdir.
fun main() {
    val sonuc = 5 carpi 2
    println(sonuc)
}

//Extension fonksiyonları daha pratik kullanmamızı sağlar.
infix fun Int.carpi(sayi:Int):Int{
    return this * sayi
}